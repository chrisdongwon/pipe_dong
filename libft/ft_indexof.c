#include "libft.h"

ssize_t	ft_indexof(char *s, int c)
{
	ssize_t	i;

	if (!s)
		return (-1);
	i = -1;
	while (s[++i])
	{
		if (s[i] == (c % 128))
			return (i);
	}
	return (-1);
}
